// Generate realistic vital signs data for the past 48 hours
const generateVitals = (baseNEWS2, hours = 48) => {
  const vitals = [];
  const now = new Date();
  
  for (let i = hours; i >= 0; i -= 2) {
    const timestamp = new Date(now.getTime() - i * 60 * 60 * 1000);
    const variation = (Math.random() - 0.5) * 2;
    const news2 = Math.max(0, Math.min(20, baseNEWS2 + variation));
    
    vitals.push({
      RecordedAt: timestamp.toISOString(),
      HeartRate: Math.round(70 + (news2 * 5) + (Math.random() - 0.5) * 10),
      RespirationRate: Math.round(16 + (news2 * 2) + (Math.random() - 0.5) * 3),
      SystolicPressure: Math.round(120 + (news2 * 3) + (Math.random() - 0.5) * 10),
      DiastolicPressure: Math.round(80 + (news2 * 2) + (Math.random() - 0.5) * 5),
      Temperature: Number((36.5 + (news2 * 0.2) + (Math.random() - 0.5) * 0.5).toFixed(1)),
      OxygenLevel: Math.round(98 - (news2 * 0.5) + (Math.random() - 0.5) * 2),
    });
  }
  
  return vitals;
};

